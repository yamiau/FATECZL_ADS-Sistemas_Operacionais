package controller;

public class Track {
	private double length;
	private double unit;
	
	public double getLength() {
		return length;
	}
	
	public double getUnit() {
		return unit;
	}

	public void setUnit(double unit) {
		this.unit = unit;
	}
	
	public void setLength(double length) {
		this.length = length;
	}
}
